# Criando uma lista vazia

lista_vazia = list()
print(lista_vazia)  # Output: []
print(type(lista_vazia))

# Criando uma lista a partir  de uma sequência
lista_numeros = list([1,2,3,4,5])
print(lista_numeros)
print(type(lista_numeros))

# criando uma lista a partir  de uma string (a string vai ser dividida em caractere)
lista_caractere = list("analista")
print(lista_caractere)


# Criando uma lista a partir de um range

lista_range = list(range(5))
print(lista_range)