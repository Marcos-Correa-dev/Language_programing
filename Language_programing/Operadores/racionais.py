# Definir valores

a = 10
b = 5

# Maior que
resultado_maior = a > b
print("Maior que: ", resultado_maior)

# Menor que
resutado_menor = a < b
print("Menor que: ", resutado_menor)

# Maior ou igual a

resultado_maior_ou_igual = a >= b
print("Maior ou igual a: ", resultado_maior_ou_igual)

# Menor ou igual a

resultado_menor_ou_igual = a <= b
print("Maior ou igual a: ", resultado_menor_ou_igual)

# Igual a

resultado_igual = a == b
print("Igual a: ", resultado_igual)

# Diferente de
resultado_diferente = a != b
print("Diferente de: ", resultado_diferente)