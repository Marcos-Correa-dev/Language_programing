# Adição

a = 10
b = 5

resultado_adicao = a + b
print("Adição:", resultado_adicao) #Output : Adição: 30


# Subtração

resultado_subtracao = a - b
print("Subtração: ", resultado_subtracao) #Output: Subtração: -10


# Multiplicação

resultado_multiplicacao = a * b
print("Multiplicação: ", resultado_multiplicacao) # Output : Multiplicacao: 200

# Divisão

resultado_divisao = a / b
print("Divisão", resultado_divisao) # Output : Divisão: 2.0


# Divisão Inteira
resultado_divisao_inteira = a // b
print("Divisão inteira", resultado_divisao_inteira) # Output: Divisão: 2

# Modulo (resto da divisão)

resultado_modulo = a % b
print("Modulo: ", resultado_modulo) # Output: Modulo: 0


# Exponenciação

resultado_exponenciacao = a ** b
print("Exponenciação: ", resultado_exponenciacao)  # Output:  Exponenciação: 10000